package org.example;

public class StoryTeller {
    // Method to tell a story about the user
    public static void tellUserStory(String name, int age) {
        System.out.println("Let me tell you a story about " + name + ".");
        System.out.println(name + " is " + age + " years old and loves learning about Java.");
        System.out.println("One day, " + name + " decided to build a chatbot to help people learn programming.");
        System.out.println("With each line of code, " + name + " became more confident in their skills.");
        System.out.println("And now, " + name + " is ready to take on new challenges and create amazing projects!");
    }
}
